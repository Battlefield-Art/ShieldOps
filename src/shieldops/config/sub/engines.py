    # ------------------------------------------------------------------
    # Decomposition fallback (RFC #241 PR-6 / #254).
    #
    # Pydantic BaseModel.__getattr__ raises AttributeError for any name not
    # declared as a model field and not present in ``model_extra``. After the
    # decomposition, 751 fields no longer live on this model, so legacy
    # ``settings.engines.<name>`` reads must fall back to the FlatSettings
    # default. Note: pydantic-v2 only invokes ``__getattr__`` after normal
    # attribute lookup misses, so this does not slow down accesses to fields
    # that are still declared above.
    # ------------------------------------------------------------------
    def __getattr__(self, name: str) -> Any:
        # ``model_extra`` always wins (env-var routed values land here).
        extra = self.__pydantic_extra__ if self.__pydantic_extra__ is not None else {}
        if name in extra:
            return extra[name]
        try:
            return _get_flat_default(name)
        except AttributeError:
            raise AttributeError(f"EnginesConfig has no attribute {name!r}") from None
