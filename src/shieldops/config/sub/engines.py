"""Engine toggle and configuration fields (Phase 12+).

This module is being incrementally decomposed (RFC #241 PR-6 / #254). 751 fields
across three categories (thresholds, *_enabled flags, retention/TTL) have been
moved out into ``shieldops.config.views.engines``. The model below still serves
as the canonical sub-config attached to ``Settings.engines`` so that legacy
``settings.engines.X`` callsites keep working — for any field that has been
deleted from the model body, ``__getattr__`` falls back to the corresponding
default on ``FlatSettings``. Routed values from the flat-env validator land in
``model_extra`` thanks to ``extra="allow"`` and shadow the FlatSettings default.
"""

from typing import Any

from pydantic import BaseModel, ConfigDict

_FLAT_DEFAULTS: Any = None


def _get_flat_default(name: str) -> Any:
    """Lazily look up a default for ``name`` on FlatSettings.

    Imported lazily to avoid pulling pydantic-settings at module import time
    and to break any potential circular import with ``config.settings``.
    """
    global _FLAT_DEFAULTS
    if _FLAT_DEFAULTS is None:
        from shieldops.config.flat import FlatSettings  # local import

        _FLAT_DEFAULTS = FlatSettings.model_fields
    field = _FLAT_DEFAULTS.get(name)
    if field is None:
        raise AttributeError(f"EnginesConfig has no attribute {name!r}")
    return field.default


